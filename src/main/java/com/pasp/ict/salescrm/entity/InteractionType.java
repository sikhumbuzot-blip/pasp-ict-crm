package com.pasp.ict.salescrm.entity;

/**
 * Enumeration defining types of customer interactions.
 */
public enum InteractionType {
    /**
     * Phone conversation
     */
    CALL,
    
    /**
     * Email communication
     */
    EMAIL,
    
    /**
     * In-person or virtual meeting
     */
    MEETING,
    
    /**
     * General note or update
     */
    NOTE
}